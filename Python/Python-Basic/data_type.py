# 演示Python的数据类型


age = 20  # 整型
name = 'Ztiany'  # 字符串
score = 32.44  # 浮点类型
isStudent = False  # 布尔类型
friends = ["张三", "李四", "王五", "赵六"]  # 列表

# 使用type函数获取变量的数据类型
print(type(age))
print(type(name))
print(type(score))
print(type(isStudent))
print(type(friends))

# 多赋值
a, b, c = 1, 3, "d"
print(a)
print(b)
print(c)
