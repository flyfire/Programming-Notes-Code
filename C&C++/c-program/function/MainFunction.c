#include <stdio.h>

//main的形式可以定义为下面方式，此时main函数可以接受字符串数组。
//argc表示数组的长度，由系统计算我们传入的参数并传递给main函数
int main(int argc, char *argv[]) {
    for (int i = 0; i < argc; ++i) {
        printf("%d %s \n", i, argv[i]);
    }
    return 0;
}

