//========================================================================
// 正则表达式
//========================================================================

//定义正则方式一：对象
var rg1 = new RegExp("^\\d+$");

//定义正则方式二：/符号，不需要字符串的转义
var rg2 = /^\d+$/;

var s = "1234";
var r1 = s.match(rg1);//String.match(正则)。返回的是匹配的结果（不是true或false），如果不匹配返回null
var r2 = rg2.test(s);//正则对象的test(字符串)，匹配返回的是true，否则是false。
console.log(r1);//->[ '1234', index: 0, input: '1234' ]
console.log(r2);//->true
